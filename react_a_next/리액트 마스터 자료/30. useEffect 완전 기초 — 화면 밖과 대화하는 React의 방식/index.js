// 30. useEffect 완전 기초 — 화면 밖과 대화하는 React의 방식
// -------------------------------------------------------------
import React, { useEffect } from "react";

export default function App() {
  // -----------------------------------------------------------
  // 1️⃣ 기본 구조 — 렌더링 후 실행
  // -----------------------------------------------------------
  useEffect(() => {
    // 컴포넌트가 최초 렌더링된 뒤 DOM 업데이트가 끝난 직후 실행될 코드
  });

  // -----------------------------------------------------------
  // 2️⃣ 정리(cleanup) 코드 포함 형태
  // -----------------------------------------------------------
  useEffect(() => {
    // 실행할 코드

    return () => {
      // 정리(cleanup) 코드
    };
  });

  // -----------------------------------------------------------
  // 3️⃣ 의존성 배열 — 특정 값에 따라 실행 제어
  // -----------------------------------------------------------
  useEffect(() => {
    // 실행할 코드
  }, [값1, 값2]);

  // -----------------------------------------------------------
  // 4️⃣ 의존성 배열이 빈 경우 (최초 1회만 실행)
  // -----------------------------------------------------------
  useEffect(() => {
    // 의존성 배열: 빈 배열 []
  }, []);

  // -----------------------------------------------------------
  // 5️⃣ 특정 값(cityName)이 변경될 때만 실행
  // -----------------------------------------------------------
  const cityName = "Seoul";
  useEffect(() => {
    // 의존성 배열: 특정 값 변화에 반응하기 (cityName)
  }, [cityName]);

  return (
    <div style={{ fontFamily: "ui-monospace, Menlo", padding: "20px" }}>
      <h2>30강. useEffect 완전 기초 — 화면 밖과 대화하는 React의 방식</h2>
      <p>콘솔에서 useEffect 실행 타이밍을 실험해보세요 🧠</p>
    </div>
  );
}
