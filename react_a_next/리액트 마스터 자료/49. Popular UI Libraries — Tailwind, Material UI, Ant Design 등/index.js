// 실무에서 널리 사용되는 UI 라이브러리를 활용한 효율적인 스타일링

export default function App() {
  return (
    <button
      className="bg-blue-500 text-white p-2 rounded hover:bg-blue-600"
    >
      클릭
    </button>
  );
}

/*
📘 강의 개요
------------------------------------------------
이번 강의에서는 CSS를 직접 작성하는 대신,
미리 만들어진 UI 라이브러리를 활용하여 
생산성을 높이는 방법을 살펴봅니다.

대표적인 예시로 Tailwind CSS, Material UI, Ant Design이 있습니다.
------------------------------------------------
*/

/*
🎨 Tailwind CSS 예시
------------------------------------------------
Tailwind는 클래스 기반의 유틸리티 CSS 프레임워크입니다.
즉, 미리 정의된 클래스를 조합해 빠르게 UI를 완성할 수 있습니다.

위 예제에서는 다음과 같은 Tailwind 클래스가 사용되었습니다:

  bg-blue-500    → 배경색 (blue 톤)
  text-white     → 글자색 (흰색)
  p-2            → 패딩 (0.5rem)
  rounded        → 모서리 둥글게
  hover:bg-blue-600 → 마우스 올렸을 때 배경색 진하게

이렇게 여러 속성을 한 줄로 조합하여 사용할 수 있습니다.
------------------------------------------------
*/

/*
💡 Tailwind의 장점
------------------------------------------------
✅ 빠른 스타일링: 클래스 조합만으로 바로 디자인 적용
✅ 불필요한 CSS 파일 감소
✅ 반응형, 다크모드, 상태 기반 스타일(hover, focus 등) 쉽게 구현
✅ JS/TS와 완벽히 통합 가능 (조건부 className 등)

❌ 단점:
- 클래스가 길어지면 가독성이 떨어질 수 있음
- 기존 CSS 문법을 그대로 사용하는 것이 아님
- 프로젝트 초기에 세팅이 필요함 (PostCSS, config 등)
------------------------------------------------
*/

/*
✨ Material UI (MUI)
------------------------------------------------
Material UI는 구글의 Material Design 시스템을 구현한
가장 대표적인 React UI 컴포넌트 라이브러리입니다.

예시:
------------------------------------------------
import Button from "@mui/material/Button";

export default function App() {
  return (
    <Button variant="contained" color="primary">
      클릭
    </Button>
  );
}
------------------------------------------------
MUI는 ‘디자인 일관성’과 ‘접근성’을 자동으로 제공하며,
테마(theme)를 통해 브랜드 컬러를 쉽게 커스터마이징할 수 있습니다.
------------------------------------------------
*/

/*
🏗️ Ant Design
------------------------------------------------
Ant Design은 중국 알리바바에서 개발한 대형 기업용 UI 프레임워크입니다.
깔끔한 디자인, 풍부한 컴포넌트, 테이블·폼 중심 기능에 강점을 가집니다.

예시:
------------------------------------------------
import { Button } from "antd";

export default function App() {
  return <Button type="primary">클릭</Button>;
}
------------------------------------------------
Ant Design은 주로 관리자 페이지(Admin Dashboard)나
비즈니스용 웹 애플리케이션에서 자주 사용됩니다.
------------------------------------------------
*/

/*
📘 핵심 요약
------------------------------------------------
- Tailwind CSS: 클래스 유틸리티 기반, 커스터마이징 자유도 높음
- Material UI: 표준화된 디자인 시스템, 손쉬운 테마 설정
- Ant Design: 비즈니스/엔터프라이즈 중심의 고급 컴포넌트 제공

💬 요약하자면,
“Tailwind는 빠른 조립, MUI는 세련된 디자인 표준, 
Ant Design은 비즈니스형 UI에 강점” 으로 기억하세요.
------------------------------------------------
*/
