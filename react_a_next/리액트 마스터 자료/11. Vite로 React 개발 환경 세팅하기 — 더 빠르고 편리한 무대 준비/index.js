/*
 * 📘 React 컴포넌트 파일 생성하기 – Test.jsx
 * --------------------------------------------------------
 * 목적:
 *   새로운 컴포넌트를 만들어 React에서 불러올 수 있도록 하는 기본 단계
 * 파일 위치:
 *   src/Test.jsx
 * 환경:
 *   Vite 기반 React 프로젝트 (my-app)
 */

// ✅ 1. 화살표 함수형 컴포넌트 선언
const Test = () => {
  // JSX 반환 (화면에 표시될 내용)
  return <h2>Test Component</h2>;
};

// ✅ 2. 컴포넌트를 외부로 내보내기 (다른 파일에서 사용 가능하게)
export default Test;

/*
📖 설명:
- React의 컴포넌트는 결국 '함수'이다.
- 함수는 JSX(HTML처럼 보이지만 실제로는 JavaScript 표현식)를 반환한다.
- export default는 “이 파일에서 기본으로 내보낼 대상”을 지정하는 문법이다.

💡 참고:
App.jsx에서 아래와 같이 불러와 사용할 수 있다.
--------------------------------------------------------
import Test from "./Test";

function App() {
  return (
    <div>
      <Test />
    </div>
  );
}
--------------------------------------------------------
*/
